class SofarStatusConverter:
    """Maps raw SOFAR Operating State register value → InverterMode value.

    SOFAR state codes (typical):
      0 = Waiting / Standby
      1 = Checking
      2 = Normal (On-Grid)
      3 = Fault
      4 = Permanent Fault
      5 = Upgrading (treated as Standby)
    """
    STATUS_MAP = {
        0: InverterMode.STANDBY,   # Waiting / Standby
        1: InverterMode.STANDBY,   # Self-checking
        2: InverterMode.ON_GRID,   # Normal / On-Grid
        3: InverterMode.FAULT,     # Fault
        4: InverterMode.FAULT,     # Permanent Fault
        5: InverterMode.STANDBY,   # Upgrading
    }

    @classmethod
    def to_inverter_mode(cls, raw):
        return cls.STATUS_MAP.get(raw, InverterMode.INITIAL)


StatusConverter = SofarStatusConverter


