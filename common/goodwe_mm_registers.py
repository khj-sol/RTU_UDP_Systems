def decode_ascii_registers(regs):
    """Decode list of U16 registers to ASCII string."""
    chars = []
    for reg in regs:
        chars.append(chr((reg >> 8) & 0xFF))
        chars.append(chr(reg & 0xFF))
    return ''.join(chars).rstrip('\x00').strip()


